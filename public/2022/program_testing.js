      "desc":"<p>Lateral prefrontal cortex (PFC) contains discrete regions that are preferentially recruited for visual versus auditory attention and working memory (WM), including regions in superior and inferior precentral sulcus and in mid inferior frontal sulcus. During auditory WM for spatial locations, these visual-biased regions are also significantly recruited. This recruitment during spatial auditory cognition may indicate that additional resources are required to buttress audition’s poor precision for spatial location (consistent with the multiple-demand account of PFC), or it may reflect task-specific recruitment of vision’s cortical machinery for representing space. In order to better understand the role of visual-biased PFC in auditory spatial cognition, we used fMRI to first label these structures in individual subjects (N = 20) using a direct contrast of visual vs. auditory 2-back WM blocks. Then, in an independent task, we estimated the recruitment of visual-biased PFC during auditory spatial WM under 3 different spatial cue conditions. Auditory locations were cued using either a weaker spatial cue (interaural time differences, ITDs), a moderate spatial cue (interaural level differences, ILDs), or a strong spatial cue (head-related transfer functions, HRTFs). The multiple-demand account predicts that visual PFC recruitment will be strongest under ITDs, because this is the case when the most effort is required to represent spatial location. Instead, we observe that visual PFC recruitment is lowest (but still positive) under ITDs, and is highest under HRTFs, suggesting that visual PFC is representing spatial location rather than allocating resources. These results support a task-specific, not multiple-demand, account of visual-biased PFC’s cognitive role.<\/p>",
